# This program shows the different ways you can print a word. 
# The method title() will print the contents of the variable with the first letter in uppercase.
# The method upper() will print the contents of the variable in all uppercase letters. 
# The method lower() will print the contents of the variable in all lowercase letters.

name = "stormborn"

print(name.title())         # first letter capitalized
print(name.upper())         # all uppercase letters
print(name.lower())         # all lowercase letters

